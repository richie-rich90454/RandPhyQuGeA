using Core.Interfaces;

namespace LaTeX.Services;

public class DummyRenderer : ILaTeXRenderer
{
    // Minimal valid 1x1 pixel PNG: 8-byte signature + IHDR + IDAT + IEND
    private static readonly byte[] _dummyPng =
    [
        // PNG signature
        0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
        // IHDR chunk (13 bytes data)
        0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
        0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
        0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
        0xDE, 0x00,
        // IDAT chunk
        0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, 0x54,
        0x08, 0xD7, 0x63, 0xF8, 0xCF, 0xC0, 0x00, 0x00,
        0x00, 0x02, 0x00, 0x01, 0xE2, 0x21, 0xBC, 0x33,
        0x00,
        // IEND chunk
        0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44,
        0xAE, 0x42, 0x60, 0x82
    ];

    public Task<byte[]> RenderToImageAsync(string latex, CancellationToken cancellationToken = default)
    {
        return Task.FromResult(_dummyPng.ToArray());
    }

    public Task<string> RenderToSvgAsync(string latex, CancellationToken cancellationToken = default)
    {
        return Task.FromResult("<svg xmlns=\"http://www.w3.org/2000/svg\"><text>DUMMY</text></svg>");
    }
}
